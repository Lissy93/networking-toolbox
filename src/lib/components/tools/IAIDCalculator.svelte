            type="button"
            class="copy-btn"
            class:copied={clipboard.isCopied('binary')}
            onclick={() => clipboard.copy(result!.binary, 'binary')}
          >
            <Icon name={clipboard.isCopied('binary') ? 'check' : 'copy'} size="xs" />
